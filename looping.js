// For Loop
for(let i = 0; i < 5; i++){
    console.log(i);
}


// For of Loop
let color = ['red','green','blue'];
for(const allcolor of color){
    console.log(allcolor);
}


// While and Do-While
let x = 1;
while(x <= 10){
    console.log(x);
    x++;
}

let y = 1;
do{
    console.log(y);
    y++;
}while(y <= 10);
